CREATE TABLE LINEITEMFACT(
L_ORDERKEY      INTEGER NOT NULL,
  L_PARTKEY       INTEGER NOT NULL,
  L_SUPPKEY       INTEGER NOT NULL,
  O_CUSTKEY      INTEGER NOT NULL,
  L_LINENUMBER    INTEGER NOT NULL,
  O_ORDERSTATUS   TEXT NOT NULL,
  O_ORDERDATE     DATE NOT NULL,
  L_QUANTITY      INTEGER NOT NULL,
  L_EXTENDEDPRICE INTEGER NOT NULL,
  L_DISCOUNT      INTEGER NOT NULL,
  L_TAX           INTEGER NOT NULL,
  L_RETURNFLAG    TEXT NOT NULL,
  L_LINESTATUS    TEXT NOT NULL,
  L_SHIPDATE      DATE NOT NULL,
  L_COMMITDATE    DATE NOT NULL,
  L_RECEIPTDATE   DATE NOT NULL,
  L_SHIPINSTRUCT  TEXT NOT NULL,
  L_SHIPMODE      TEXT NOT NULL,
  L_COMMENT       TEXT NOT NULL,
  LINEITEM_REVENUE  INTEGER NOT NULL,
  FOREIGN KEY (O_CUSTKEY) REFERENCES CUSTOMERDIM(C_CUSTKEY),
  FOREIGN KEY (L_PARTKEY, L_SUPPKEY) REFERENCES PARTSUPPLIERDIM(PS_PARTKEY, PS_SUPPKEY),
  FOREIGN KEY (L_ORDERKEY) REFERENCES ORDERS(O_ORDERKEY)
)